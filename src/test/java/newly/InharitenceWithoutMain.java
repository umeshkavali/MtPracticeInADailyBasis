package newly;

public class InharitenceWithoutMain 
{
	
	
	// without parameters and without return value
	public void m1()
	{
		System.out.println("Hello This is m1 from InharitenceWithoutMain Class");
	}
	
	// without parameters with return value
	public String m2()
	{
		return "Hello This is m2 from InharitenceWithoutMain Class";
	}
	
	// With parameters no return value
	
	public void m3(String name)
	{
		System.out.println("Hello "+name);
	}
	
	// with perameters with return value
	public String m4(String name2)
	{
		return "Hello "+name2;
		
	}
}
